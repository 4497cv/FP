void LCD_terminal_startup()
{
	terminal_config_state_t current_state;

	current_state = terminal_S0; //set initial state

	FSM_LCD_t FSM_terminal_start[FSM_LCD_STATES]=
	{
		{&Logo[0],  {terminal_S1, terminal_S2, terminal_S3, terminal_S4, terminal_S5, terminal_S0}}
	};

	LCD_nokia_clear();

	do
	{
		/* Send to terminal the state's current text */
		LCD_nokia_bitmap(FSM_terminal_start[current_state].bitmap);
		/* get next state */
		current_state = FSM_terminal_start[current_state].next[INDEX_INIT];
	}while(terminal_S1 != current_state); //repeat while all the states are completed

}

