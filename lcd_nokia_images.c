{
	LCD_nokia_clear();
	switch(sequence_state)
	{
		case SEQUENCE_ZERO:
			switch(note_state)
			{
			case buffer_S1:
				LCD_nokia_bitmap(&pentagram_sequence0_C[0]);
			break;
			case buffer_S2:
				LCD_nokia_bitmap(&pentagram_sequence0_C_D[0]);
			break;
			case buffer_S3:
				LCD_nokia_bitmap(&pentagram_sequence0_C_D_E[0]);
			break;
			case buffer_S4:
				LCD_nokia_bitmap(&pentagram_sequence0_C_D_E_G[0]);
			break;
			case buffer_S5:
				LCD_nokia_bitmap(&pentagram_sequence0_C_D_E_G_A[0]);
			break;
			}
		break;
		case SEQUENCE_ONE:
		break;
		case SEQUENCE_TWO:
		break;
		case SEQUENCE_THREE:
		break;
		case SEQUENCE_FOUR:
		break;
		case SEQUENCE_FIVE:
		break;
	}
}
